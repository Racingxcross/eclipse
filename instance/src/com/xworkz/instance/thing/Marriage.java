package com.xworkz.instance.thing;

public class Marriage {
	public static String groomName;
	public static String brideName;
	public static String marriageType;

	public Marriage(String groomName, String brideName, String marriageType) {
		super();
		Marriage.groomName = groomName;
		Marriage.brideName = brideName;
		this.marriageType = marriageType;
	}
}